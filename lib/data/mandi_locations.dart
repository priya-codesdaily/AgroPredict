  static Map<String, double>? getCoordinates(String mandiName) {
    String cleaned = mandiName
        .replaceAll(RegExp(r'\(.*?\)'), '')
        .replaceAll('APMC', '')
        .replaceAll('Mandi', '')
        .replaceAll('Market', '')
        .replaceAll('Yard', '')
        .replaceAll('Sandhai', '')
        .replaceAll('Sabji', '')
        .replaceAll(RegExp(r'\s+'), ' ')
        .trim();

    if (coordinates.containsKey(mandiName)) return coordinates[mandiName];
    if (coordinates.containsKey(cleaned)) return coordinates[cleaned];

    for (final entry in coordinates.entries) {
      final cityLower = entry.key.toLowerCase();
      final mandiLower = cleaned.toLowerCase();
      if (mandiLower.contains(cityLower) || cityLower.contains(mandiLower)) {
        return entry.value;
      }
      final firstWord = mandiLower.split(' ').first;
      if (firstWord.length > 3 && cityLower.startsWith(firstWord)) {
        return entry.value;
      }
    }
    return null;
  }
